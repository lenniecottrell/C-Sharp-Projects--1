using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Step206
{
    public class Math
    {
        public static int addTen(int inputNum)
        {
            return inputNum + 10;
        }
        public static int timesTen(int inputNum)
        {
            return inputNum * 10;
        }
        public static int minusTen(int inputNum)
        {
            return inputNum - 10;
        }
    }
}
